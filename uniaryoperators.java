package com.example.vscode;

public class uniaryoperators {
    public static void main(String[] args) {
        int numb=1;
        System.out.println(numb++); //1
        System.out.println(numb);//2
        System.out.println(++numb);//3
        System.out.println(numb);//3
       System.out.println(numb--);//3
       System.out.println(numb);//2
    System.out.println(--numb);//1
    System.out.println(numb);//1

    }

}
