package com.example.vscode;

public class loops2 {
    public static void main(String[] args) {
        //while loop
        int j=100;
        while ((j>=1)) {
            System.out.println(j);
            j=j-1;
        }
        //do while
        int k=100;
        do{
            System.out.println(k);
            k=k-1;
        }
        while(k>=1);
    }
}
